    private void btnEliminarIndiceActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarIndiceActionPerformed
    
        
       if(Lista.TamañoLista!=0){
        
        int Indice = Integer.parseInt(txtEliminarIndice.getText());
        
        if(Indice>Lista.TamañoLista){
            JOptionPane.showMessageDialog(null, "Ingrese"
        + " un valor menor al tamaño de la lista TAMAÑO LISTA: "+Lista.TamañoLista);
        }
        else{
            Lista.EliminarPerroPorIndice(Indice);
            Graficar();
            Estadisticas();
        }
        
        
       }else{
               JOptionPane.showMessageDialog(null, "La cola esta vacia");
       }
        
    }//GEN-LAST:event_btnEliminarIndiceActionPerformed

