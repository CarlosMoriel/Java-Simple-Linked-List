     public void Limpiar(){
         PanelContenedor.removeAll();
         Revalidar();
    }//Fin Limpiar
     
    public void Revalidar(){
        PanelContenedor.revalidate();
        PanelContenedor.repaint();

        sPane.revalidate();
        sPane.repaint();
     }//Fin Revalidar
    
    public void Graficar(){
        //Limpiar panel primero
        Limpiar();
        
        if(Lista.ListaVacia()){
            
        }else{
        ArrayList<Canino> Elementos = Lista.ObtenerElementosGRAFICACION();
        
       if(Elementos == null){
           
       }else{
//        for (Canino Elemento : Elementos) {
//            System.out.println(Elemento.dato);
//        }
        
        int ContadorNulos = 1;
        for (Canino Elemento : Elementos) {
           
            JPanel PanelEtiqueta = new JPanel();
            PanelEtiqueta.setSize(new Dimension(100,50));
            PanelEtiqueta.setMaximumSize(new Dimension(100,50));
            PanelEtiqueta.setMaximumSize(new Dimension(100,50));
            if(Elemento == Lista.elementoActual) PanelEtiqueta.setBackground(Color.ORANGE);
            else PanelEtiqueta.setBackground(Color.CYAN);
            
            JLabel Etiqueta = new JLabel();
            Etiqueta.setText((String) Elemento.ObtenerValorPerro());
            Etiqueta.setBounds(0,0,20,15);
            Font fuente = new Font("Calibri", 3, 20);
            Etiqueta.setFont(fuente);
            
            JPanel PanelFlecha = new JPanel();
            PanelFlecha.setSize(new Dimension(100,50));
            PanelFlecha.setMaximumSize(new Dimension(100,50));
            PanelFlecha.setMaximumSize(new Dimension(100,50));
            
            JLabel Flecha = new JLabel();
            Flecha.setBounds(50, 25, 50, 50);
            Icon icono = new ImageIcon("src/Recursos/flecha.png");
            Flecha.setIcon(icono);
            
            PanelFlecha.add(Flecha);
            PanelEtiqueta.add(Etiqueta);
            
            PanelContenedor.add(PanelEtiqueta);
            PanelContenedor.add(PanelFlecha);
            
            if(ContadorNulos == Lista.TamañoLista){
                JPanel PanelNullo = new JPanel();
                PanelNullo.setSize(new Dimension(100,50));
                PanelNullo.setMaximumSize(new Dimension(100,50));
                PanelNullo.setMaximumSize(new Dimension(100,50));
                PanelNullo.setBackground(Color.RED);
               
            
                JLabel EtiquetaNullo = new JLabel();
                EtiquetaNullo.setText("Null");
                EtiquetaNullo.setBounds(0,0,20,15);
                Font fuente2 = new Font("Calibri", 3, 20);
                EtiquetaNullo.setFont(fuente2);
                
                PanelNullo.add(EtiquetaNullo);
                PanelContenedor.add(PanelNullo);
            }
            ContadorNulos++;
        }//Fin For
        Revalidar();
       }
      }
    }//Fin Graficar
    
    public void Estadisticas(){
      if(Lista.ListaVacia()){
        this.lbPrimerelemento.setText("Null");
        this.lbElementoActual.setText("Null");
        this.lbUlimoElemento.setText("Null");
        this.lbTamaño.setText(""+Lista.TamañoLista);
      }else{
        this.lbPrimerelemento.setText(""+Lista.inicioLista.ObtenerValorPerro());
        this.lbElementoActual.setText(Lista.elementoActual.ObtenerValorPerro().toString());
        this.lbUlimoElemento.setText((String) Lista.finLista.ObtenerValorPerro());
        this.lbTamaño.setText(""+Lista.TamañoLista);
      }
    }
    
